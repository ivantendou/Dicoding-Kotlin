// 04 - While and Do While

// While
fun main() {
    var counter = 1
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }
}
/*
   output :
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
*/

// Do While
fun main() {
    var counter = 1
    do {
        println("Hello, World!")
        counter++
    } while (counter <= 7)
}
 
/*
   output:
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
       Hello, World!
*/
